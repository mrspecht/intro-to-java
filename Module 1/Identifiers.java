// Identifiers & naming convention

/*
  An identifier is a programmer-defined name that represents some element of
  a program

  The name must begin with a: letter, '$', or '_'
  Subsequent characters may also be numbers
  You cannot use the same name as a Java reserved word

  Method and variables names begin with a lowercase letter followed by
  CamelCase. Class names begin with an uppercase letter followed by CamelCase

  Another style you might see is called 'snake_case'
*/

public class Identifiers {
  public static void main(String[] args) {
    int num = 1;
    int __n = 2;
    int _33 = 3;
    int _$_ = 4;
    int $05 = 5;
    int Num = 6;
  }
}
