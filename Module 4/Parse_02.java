// Parsing numbers

import java.util.*;

public class Parse_02 {
  public static void main(String[] args) {
    System.out.println(new Scanner("16").nextByte());
    System.out.println(new Scanner("42").nextInt());
    System.out.println(new Scanner("1123456789").nextLong());
    System.out.println(new Scanner("4.2").nextFloat());
    System.out.println(new Scanner("99.678").nextDouble());
    System.out.println(new Scanner("true").nextBoolean());
  }
}
